require 'hpricot';
require 'open-uri'

%w{Death+Knight Druid Hunter Mage Paladin Priest Rogue Shaman Warlock Warrior}.each do |c|
  puts "\n\n#{c}\n#{'-'*c.length}"
  majors = []
  minors = []
  html = Hpricot(open("http://www.wowpopular.com/#{c}/Glyphs"))
  (html / ("#detailsbody table table table tr")).map do |e|
    link = e.to_s.match(/wowhead\.com\/\?item=(\d+)/)
    if link
      if e.to_s.match(/Major/)
        majors << link[1]
      else
        minors << link[1]
      end
    end
  end
  puts minors
  puts "  0  "
  puts majors
end

